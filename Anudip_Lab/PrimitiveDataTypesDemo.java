public class PrimitiveDataTypesDemo {
    public static void main(String[] args) {
        // Declaring and initializing all primitive data types

        // byte: A small integer (from -128 to 127)
        byte age = 25;

        // short: A larger integer (from -32,768 to 32,767)
        short year = 2024;

        // int: A standard integer (from -2^31 to 2^31-1)
        int population = 1000000;

        // long: A larger integer (from -2^63 to 2^63-1)
        long distanceToMoon = 384400000L;

        // float: A single-precision floating point number
        float price = 19.99f;

        // double: A double-precision floating point number
        double piValue = 3.14159;

        // char: A single character
        char grade = 'A';

        // boolean: Represents true or false
        boolean isJavaFun = true;

        // Printing the values of all variables
        System.out.println("Byte value (age): " + age);
        System.out.println("Short value (year): " + year);
        System.out.println("Integer value (population): " + population);
        System.out.println("Long value (distance to moon): " + distanceToMoon);
        System.out.println("Float value (price): " + price);
        System.out.println("Double value (pi): " + piValue);
        System.out.println("Character value (grade): " + grade);
        System.out.println("Boolean value (is Java fun?): " + isJavaFun);
    }
}
